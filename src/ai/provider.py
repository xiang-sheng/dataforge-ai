# Abstract base provider
# ---------------------------------------------------------------------------

class BaseAIProvider(ABC):
    """Abstract base class that every AI provider must implement.

    Subclasses are responsible for translating the unified interface into
    provider-specific API calls and mapping responses back to ``AIResponse``
    and ``StreamChunk`` objects.
    """

    def __init__(self, config: ProviderConfig) -> None:
        self._config = config
        self._cumulative_usage = TokenUsage()
        self._rate_limiter = RateLimiter(
            rpm=config.rate_limit_rpm,
            tpm=config.rate_limit_tpm,
        )
        self._request_count: int = 0

    # -- Properties ---------------------------------------------------------

    @property
    def config(self) -> ProviderConfig:
        """Return the active provider configuration."""
        return self._config

    @property
    def cumulative_usage(self) -> TokenUsage:
        """Return cumulative token usage since this provider was created."""
        return self._cumulative_usage

    @property
    def request_count(self) -> int:
        """Return total number of requests made through this provider."""
        return self._request_count

    # -- Abstract interface -------------------------------------------------

    @abstractmethod
    async def generate(
        self,
        prompt: str,
        *,
        system: Optional[str] = None,
        temperature: Optional[float] = None,
        max_tokens: Optional[int] = None,
        stop: Optional[Sequence[str]] = None,
        **kwargs: Any,
    ) -> AIResponse:
        """Generate a completion from a single prompt string.

        Args:
            prompt: The user prompt.
            system: Optional system-level instruction prepended to the request.
            temperature: Override the default sampling temperature.
            max_tokens: Override the default maximum output tokens.
            stop: Optional sequence of stop strings.
            **kwargs: Provider-specific parameters.

        Returns:
            AIResponse with the generated text and metadata.
        """

    @abstractmethod
    async def chat(
        self,
        messages: List[ChatMessage],
        *,
        temperature: Optional[float] = None,
        max_tokens: Optional[int] = None,
        stop: Optional[Sequence[str]] = None,
        **kwargs: Any,
    ) -> AIResponse:
        """Generate a completion from a multi-turn chat conversation.

        Args:
            messages: Ordered list of ``ChatMessage`` objects representing the
                conversation history.
            temperature: Override the default sampling temperature.
            max_tokens: Override the default maximum output tokens.
            stop: Optional sequence of stop strings.
            **kwargs: Provider-specific parameters.

        Returns:
            AIResponse with the generated text and metadata.
        """

    @abstractmethod
    async def complete(
        self,
        prompt: str,
        suffix: Optional[str] = None,
        *,
        temperature: Optional[float] = None,
        max_tokens: Optional[int] = None,
        stop: Optional[Sequence[str]] = None,
        **kwargs: Any,
    ) -> AIResponse:
        """Perform a raw text completion (insert / fill-in-the-middle).

        This is useful for code-completion style tasks where both a prefix and
        a suffix surround the gap to be filled.

        Args:
            prompt: The text *before* the gap.
            suffix: Optional text *after* the gap.
            temperature: Override the default sampling temperature.
            max_tokens: Override the default maximum output tokens.
            stop: Optional sequence of stop strings.
            **kwargs: Provider-specific parameters.

        Returns:
            AIResponse with the generated text and metadata.
        """

    @abstractmethod
    async def stream_generate(
        self,
        prompt: str,
        *,
        system: Optional[str] = None,
        temperature: Optional[float] = None,
        max_tokens: Optional[int] = None,
        **kwargs: Any,
    ) -> AsyncIterator[StreamChunk]:
        """Stream a completion from a single prompt, yielding chunks.

        Args:
            prompt: The user prompt.
            system: Optional system-level instruction.
            temperature: Override the default sampling temperature.
            max_tokens: Override the default maximum output tokens.
            **kwargs: Provider-specific parameters.

        Yields:
            StreamChunk objects as they arrive from the provider.
        """

    @abstractmethod
    async def stream_chat(
        self,
        messages: List[ChatMessage],
        *,
        temperature: Optional[float] = None,
        max_tokens: Optional[int] = None,
        **kwargs: Any,
    ) -> AsyncIterator[StreamChunk]:
        """Stream a chat completion, yielding chunks.

        Args:
            messages: The conversation messages.
            temperature: Override the default sampling temperature.
            max_tokens: Override the default maximum output tokens.
            **kwargs: Provider-specific parameters.

        Yields:
            StreamChunk objects as they arrive from the provider.
        """

    # -- Shared helpers -----------------------------------------------------

    def _record_usage(self, usage: TokenUsage) -> None:
        """Accumulate token usage and increment the request counter."""
        self._cumulative_usage = self._cumulative_usage + usage
        self._request_count += 1

    async def _retry_with_backoff(
        self,
        fn: Callable[..., Any],
        *args: Any,
        **kwargs: Any,
    ) -> Any:
        """Execute *fn* with exponential-backoff retry logic.

        Args:
            fn: An async callable to execute.
            *args: Positional arguments forwarded to *fn*.
            **kwargs: Keyword arguments forwarded to *fn*.

        Returns:
            The return value of *fn*.

        Raises:
            Exception: The last exception if all retries are exhausted.
        """
        last_exc: Optional[Exception] = None
        for attempt in range(1, self._config.max_retries + 1):
            try:
                return await fn(*args, **kwargs)
            except Exception as exc:
                last_exc = exc
                delay = self._config.retry_delay * (2 ** (attempt - 1))
                logger.warning(
                    "Provider call failed (attempt %d/%d): %s  -- retrying in %.1fs",
                    attempt,
                    self._config.max_retries,
                    exc,
                    delay,
                )
                await asyncio.sleep(delay)
        raise last_exc  # type: ignore[misc]


