# Rate limiter
# ---------------------------------------------------------------------------

class RateLimiter:
    """A simple sliding-window rate limiter for API calls.

    Enforces both requests-per-minute (RPM) and tokens-per-minute (TPM)
    limits using an in-memory sliding window.

    Args:
        rpm: Maximum requests per minute.  ``0`` disables RPM limiting.
        tpm: Maximum tokens per minute.  ``0`` disables TPM limiting.
    """

    def __init__(self, rpm: int = 0, tpm: int = 0) -> None:
        self._rpm = rpm
        self._tpm = tpm
        self._request_timestamps: list[float] = []
        self._token_records: list[tuple[float, int]] = []
        self._lock = asyncio.Lock()

    @staticmethod
    def _prune_window(records: list, window_seconds: float = 60.0) -> None:
        """Remove entries older than *window_seconds* from *records* in-place."""
        cutoff = time.monotonic() - window_seconds
        while records and records[0] < cutoff:
            records.pop(0)

    async def acquire(self, estimated_tokens: int = 0) -> None:
        """Block until the caller is allowed to proceed.

        Args:
            estimated_tokens: The estimated token cost of the upcoming request,
                used for TPM limiting.
        """
        async with self._lock:
            # --- RPM check ---
            if self._rpm > 0:
                self._prune_window(self._request_timestamps)
                while len(self._request_timestamps) >= self._rpm:
                    sleep_for = self._request_timestamps[0] - (time.monotonic() - 60.0)
                    if sleep_for > 0:
                        logger.debug("Rate limiter: sleeping %.2fs (RPM limit)", sleep_for)
                        await asyncio.sleep(sleep_for)
                    self._prune_window(self._request_timestamps)
                self._request_timestamps.append(time.monotonic())

            # --- TPM check ---
            if self._tpm > 0 and estimated_tokens > 0:
                self._prune_window(self._token_records)
                current_tpm = sum(t for _, t in self._token_records)
                while current_tpm + estimated_tokens > self._tpm:
                    sleep_for = self._token_records[0][0] - (time.monotonic() - 60.0)
                    if sleep_for > 0:
                        logger.debug("Rate limiter: sleeping %.2fs (TPM limit)", sleep_for)
                        await asyncio.sleep(sleep_for)
                    self._prune_window(self._token_records)
                    current_tpm = sum(t for _, t in self._token_records)
                self._token_records.append((time.monotonic(), estimated_tokens))

    def record_usage(self, tokens_used: int) -> None:
        """Record actual token consumption after a request completes.

        This is a fire-and-forget synchronous call; the records are consumed by
        the next ``acquire`` check.

        Args:
            tokens_used: The actual number of tokens consumed.
        """
        self._token_records.append((time.monotonic(), tokens_used))


