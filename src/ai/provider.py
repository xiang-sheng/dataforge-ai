class ModelProvider(StrEnum):
    """Supported AI model providers."""

    OPENAI = "openai"
    ANTHROPIC = "anthropic"
    AZURE_OPENAI = "azure_openai"
    LOCAL = "local"
    OLLAMA = "ollama"
    TONGYI = "tongyi"
    DEEPSEEK = "deepseek"


@dataclass
class TokenUsage:
    """Tracks token consumption for a single request or accumulated usage.

    Attributes:
        prompt_tokens: Number of tokens in the prompt / input.
        completion_tokens: Number of tokens in the completion / output.
        total_tokens: Total tokens consumed (prompt + completion).
    """

    prompt_tokens: int = 0
    completion_tokens: int = 0
    total_tokens: int = 0

    def __add__(self, other: TokenUsage) -> TokenUsage:
        return TokenUsage(
            prompt_tokens=self.prompt_tokens + other.prompt_tokens,
            completion_tokens=self.completion_tokens + other.completion_tokens,
            total_tokens=self.total_tokens + other.total_tokens,
        )


@dataclass
class ChatMessage:
    """A single message in a chat conversation.

    Attributes:
        role: The role of the message sender (system, user, assistant).
        content: The textual content of the message.
        name: Optional name identifier for the speaker.
    """

    role: str  # "system" | "user" | "assistant"
    content: str
    name: str | None = None

    def to_dict(self) -> dict[str, Any]:
        """Serialize the message to a plain dictionary."""
        payload: dict[str, Any] = {"role": self.role, "content": self.content}
        if self.name:
            payload["name"] = self.name
        return payload

    def to_langchain_message(self) -> BaseMessage:
        """Convert to the corresponding LangChain message type."""
        if self.role == "system":
            return SystemMessage(content=self.content)
        if self.role == "assistant":
            return AIMessage(content=self.content)
        # Default: treat as human / user message
        return HumanMessage(content=self.content)


@dataclass
class AIResponse:
    """Unified response object returned by every provider.

    Attributes:
        content: The textual response generated by the model.
        model: The model identifier that produced the response.
        usage: Token consumption statistics for this request.
        finish_reason: The reason the model stopped generating (e.g. ``stop``,
            ``length``, ``content_filter``).
        raw: The raw provider-specific response payload, kept for debugging or
            advanced post-processing.
    """

    content: str
    model: str
    usage: TokenUsage
    finish_reason: str | None = None
    raw: Any | None = None


@dataclass
class StreamChunk:
    """A single chunk emitted during streaming generation.

    Attributes:
        delta: The incremental text produced in this chunk.
        finish_reason: Set when this chunk signals the end of generation.
        usage: Token usage, typically only populated in the final chunk.
    """

    delta: str = ""
    finish_reason: str | None = None
    usage: TokenUsage | None = None


@dataclass
class ProviderConfig:
    """Configuration bundle for an AI provider instance.

    Attributes:
        provider: Which provider backend to use.
        api_key: The API key (or ``None`` for local models).
        base_url: Optional custom base URL for the provider API.
        model: Default model identifier (e.g. ``gpt-4o``, ``claude-3-opus``).
        temperature: Sampling temperature controlling randomness.
        max_tokens: Maximum tokens to generate in a single response.
        timeout: Per-request timeout in seconds.
        max_retries: Number of automatic retries on transient failures.
        retry_delay: Base delay between retries in seconds (exponential back-off).
        rate_limit_rpm: Maximum requests per minute (``0`` = unlimited).
        rate_limit_tpm: Maximum tokens per minute (``0`` = unlimited).
        extra: Provider-specific keyword arguments forwarded to the client.
    """

    provider: ModelProvider = ModelProvider.OPENAI
    api_key: str | None = None
    base_url: str | None = None
    model: str = "gpt-4o"
    temperature: float = 0.2
    max_tokens: int = 4096
    timeout: float = 60.0
    max_retries: int = 3
    retry_delay: float = 1.0
    rate_limit_rpm: int = 60
    rate_limit_tpm: int = 100_000
    extra: dict[str, Any] = field(default_factory=dict)


