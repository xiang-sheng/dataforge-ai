# -*- coding: utf-8 -*-
"""
DataForge AI - AI provider abstraction layer.

Provides a unified interface for interacting with multiple LLM backends
(OpenAI, Anthropic, local models, etc.) with built-in rate limiting,
automatic retry, token-usage tracking, and streaming support.
"""

from __future__ import annotations

import asyncio
import logging
import time
from abc import ABC, abstractmethod
from dataclasses import dataclass, field
from enum import Enum
from typing import (
    Any,
    AsyncIterator,
    Callable,
    Dict,
    List,
    Optional,
    Sequence,
    Union,
)

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Data models
# ---------------------------------------------------------------------------

class ModelProvider(str, Enum):
    """Supported AI model providers."""

    OPENAI = "openai"
    ANTHROPIC = "anthropic"
    AZURE_OPENAI = "azure_openai"
    LOCAL = "local"


@dataclass
class TokenUsage:
    """Tracks token consumption for a single request or accumulated usage.

    Attributes:
        prompt_tokens: Number of tokens in the prompt / input.
        completion_tokens: Number of tokens in the completion / output.
        total_tokens: Total tokens consumed (prompt + completion).
    """

    prompt_tokens: int = 0
    completion_tokens: int = 0
    total_tokens: int = 0

    def __add__(self, other: "TokenUsage") -> "TokenUsage":
        return TokenUsage(
            prompt_tokens=self.prompt_tokens + other.prompt_tokens,
            completion_tokens=self.completion_tokens + other.completion_tokens,
            total_tokens=self.total_tokens + other.total_tokens,
        )


@dataclass
class ChatMessage:
    """A single message in a chat conversation.

    Attributes:
        role: The role of the message sender (system, user, assistant).
        content: The textual content of the message.
        name: Optional name identifier for the speaker.
    """

    role: str  # "system" | "user" | "assistant"
    content: str
    name: Optional[str] = None

    def to_dict(self) -> Dict[str, Any]:
        """Serialize the message to a plain dictionary."""
        payload: Dict[str, Any] = {"role": self.role, "content": self.content}
        if self.name:
            payload["name"] = self.name
        return payload


@dataclass
class AIResponse:
    """Unified response object returned by every provider.

    Attributes:
        content: The textual response generated by the model.
        model: The model identifier that produced the response.
        usage: Token consumption statistics for this request.
        finish_reason: The reason the model stopped generating (e.g. ``stop``,
            ``length``, ``content_filter``).
        raw: The raw provider-specific response payload, kept for debugging or
            advanced post-processing.
    """

    content: str
    model: str
    usage: TokenUsage
    finish_reason: Optional[str] = None
    raw: Optional[Any] = None


@dataclass
class StreamChunk:
    """A single chunk emitted during streaming generation.

    Attributes:
        delta: The incremental text produced in this chunk.
        finish_reason: Set when this chunk signals the end of generation.
        usage: Token usage, typically only populated in the final chunk.
    """

    delta: str = ""
    finish_reason: Optional[str] = None
    usage: Optional[TokenUsage] = None


@dataclass
class ProviderConfig:
    """Configuration bundle for an AI provider instance.

    Attributes:
        provider: Which provider backend to use.
        api_key: The API key (or ``None`` for local models).
        base_url: Optional custom base URL for the provider API.
        model: Default model identifier (e.g. ``gpt-4o``, ``claude-3-opus``).
        temperature: Sampling temperature controlling randomness.
        max_tokens: Maximum tokens to generate in a single response.
        timeout: Per-request timeout in seconds.
        max_retries: Number of automatic retries on transient failures.
        retry_delay: Base delay between retries in seconds (exponential back-off).
        rate_limit_rpm: Maximum requests per minute (``0`` = unlimited).
        rate_limit_tpm: Maximum tokens per minute (``0`` = unlimited).
        extra: Provider-specific keyword arguments forwarded to the client.
    """

    provider: ModelProvider = ModelProvider.OPENAI
    api_key: Optional[str] = None
    base_url: Optional[str] = None
    model: str = "gpt-4o"
    temperature: float = 0.2
    max_tokens: int = 4096
    timeout: float = 60.0
    max_retries: int = 3
    retry_delay: float = 1.0
    rate_limit_rpm: int = 60
    rate_limit_tpm: int = 100_000
    extra: Dict[str, Any] = field(default_factory=dict)


# ---------------------------------------------------------------------------
# Rate limiter
# ---------------------------------------------------------------------------

class RateLimiter:
    """A simple sliding-window rate limiter for API calls.

    Enforces both requests-per-minute (RPM) and tokens-per-minute (TPM)
    limits using an in-memory sliding window.

    Args:
        rpm: Maximum requests per minute.  ``0`` disables RPM limiting.
        tpm: Maximum tokens per minute.  ``0`` disables TPM limiting.
    """

    def __init__(self, rpm: int = 0, tpm: int = 0) -> None:
        self._rpm = rpm
        self._tpm = tpm
        self._request_timestamps: List[float] = []
        self._token_records: List[tuple[float, int]] = []
        self._lock = asyncio.Lock()

    @staticmethod
    def _prune_window(records: List, window_seconds: float = 60.0) -> None:
        """Remove entries older than *window_seconds* from *records* in-place."""
        cutoff = time.monotonic() - window_seconds
        while records and records[0] < cutoff:
            records.pop(0)

    async def acquire(self, estimated_tokens: int = 0) -> None:
        """Block until the caller is allowed to proceed.

        Args:
            estimated_tokens: The estimated token cost of the upcoming request,
                used for TPM limiting.
        """
        async with self._lock:
            # --- RPM check ---
            if self._rpm > 0:
                self._prune_window(self._request_timestamps)
                while len(self._request_timestamps) >= self._rpm:
                    sleep_for = self._request_timestamps[0] - (time.monotonic() - 60.0)
                    if sleep_for > 0:
                        logger.debug("Rate limiter: sleeping %.2fs (RPM limit)", sleep_for)
                        await asyncio.sleep(sleep_for)
                    self._prune_window(self._request_timestamps)
                self._request_timestamps.append(time.monotonic())

            # --- TPM check ---
            if self._tpm > 0 and estimated_tokens > 0:
                self._prune_window(self._token_records)
                current_tpm = sum(t for _, t in self._token_records)
                while current_tpm + estimated_tokens > self._tpm:
                    sleep_for = self._token_records[0][0] - (time.monotonic() - 60.0)
                    if sleep_for > 0:
                        logger.debug("Rate limiter: sleeping %.2fs (TPM limit)", sleep_for)
                        await asyncio.sleep(sleep_for)
                    self._prune_window(self._token_records)
                    current_tpm = sum(t for _, t in self._token_records)
                self._token_records.append((time.monotonic(), estimated_tokens))

    def record_usage(self, tokens_used: int) -> None:
        """Record actual token consumption after a request completes.

        This is a fire-and-forget synchronous call; the records are consumed by
        the next ``acquire`` check.

        Args:
            tokens_used: The actual number of tokens consumed.
        """
        self._token_records.append((time.monotonic(), tokens_used))


