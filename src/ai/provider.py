class AIProviderFactory:
    """Factory for creating AI provider instances from configuration.

    Usage::

        config = ProviderConfig(provider=ModelProvider.OPENAI, api_key="sk-...")
        provider = AIProviderFactory.create(config)
        response = await provider.generate("Write a SQL query to count users.")
    """

    _registry: Dict[ModelProvider, type] = {
        ModelProvider.OPENAI: OpenAIProvider,
        ModelProvider.ANTHROPIC: AnthropicProvider,
    }

    @classmethod
    def create(cls, config: ProviderConfig) -> BaseAIProvider:
        """Instantiate and return the appropriate provider.

        Args:
            config: Provider configuration specifying which backend to use.

        Returns:
            A concrete ``BaseAIProvider`` subclass instance.

        Raises:
            ValueError: If the requested provider is not registered.
        """
        provider_cls = cls._registry.get(config.provider)
        if provider_cls is None:
            raise ValueError(
                f"Unsupported AI provider: {config.provider.value}.  "
                f"Registered providers: {list(cls._registry.keys())}"
            )
        return provider_cls(config)

    @classmethod
    def register(cls, provider: ModelProvider, provider_cls: type) -> None:
        """Register a custom provider class.

        Args:
            provider: The enum value to associate with the class.
            provider_cls: A ``BaseAIProvider`` subclass.
        """
        cls._registry[provider] = provider_cls
